var sum = 0;
for (var i=1;i<=15;i++) { // Начальное значение счётчика; условие цикла; приращение счётчика
	if (i==5 || i==7) continue; // Если (i == 5 или i == 7), то оператор continue перейдёт к следующей интерации цикла, не завершив текущую.
	sum = sum + i;  // Выполняется цикл
}
alert(sum); // Выводится полученный ответ