var a = prompt("Введите число 11", "0");
//После ввода числа 11 получаем ответ: "Введенное значение верно"
if (a == 11) {
	document.write("Введенное значение верно");
}
// После ввода любого числа кроме 11 выдаёт: "Введенное значение неверно."
else {
   document.write("Введенное значение неверно.");
}
