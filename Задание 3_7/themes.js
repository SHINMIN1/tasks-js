var value = "2"; // Заданное значение
switch (value) {
// блок операторов
	case "1": // if (value === 1)
	case "2": // if (value === 2)
	case "3": // if (value === 3)
	document.write("Hello"); // тогда выводим...
	break;
	case "4": // if (value === 4)
	case "5": // if (value === 5)
	document.write("World"); // тогда выводим...
	break;
	default: //else
	document.write("Error"); // иначе выводим...
}